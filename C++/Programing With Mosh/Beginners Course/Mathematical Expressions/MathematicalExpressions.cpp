#include <iostream>

int main() {
    int x = 10;
    int y = 3;
    x++; //Increment By One
    x--; //Decrement By One
    std::cout << x + y; // Add 2 variables
    std::cout << x - y; // Subtract 2 Variables
    std::cout << x * y; // Multiply 2 Variable
    std::cout << x / y; // Divide 2 Variables
    std::cout << x % y; // Get The Remainder of 2 Variables Division

    return 0;
}